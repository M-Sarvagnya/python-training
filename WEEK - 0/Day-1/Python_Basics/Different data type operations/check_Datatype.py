#String Data Type

x = input("Enter a Value: ")
print("The Data type of X is ",type(x))

#Int Data Type
y = int(input("Enter b value: "))
print("The Data type of X is ",type(y))

#Float Data Type
Z = complex(input("Enter Z value: "))
print("The Data type of Z is ",type(Z))

#Complext Data Type
C = complex(input("Enter C value: "))
print("The Data type of C is ",type(C))

#List Data Type
L = input("Enter L value:").split(',')
print("The Data type of L is",type(L))

#Tuple Data Type
T = tuple(input("Enter T value:").split())
print("The Data type of T is",type(T))

# Boolean Data Type
B = bool(input("Enter B value:").split())
print("The Data type of B is",type(B))

